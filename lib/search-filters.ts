export const FOOD_FAMILY_PRIMARY_TYPES = new Set([
  ...Array.from(SPECIFIC_FOOD_PRIMARY_TYPES),
  "restaurant", "japanese_restaurant", "food", "meal_takeaway", "meal_delivery", "food_court",
]);
export function primaryTypeAllowedForGenre(primaryType: string | undefined, deepDiveRaw: string): boolean {
  const deepDive = canonDeepDive(deepDiveRaw);   // L1短縮形を正規化
  // 楽しみたい系: 飲食店型は全て除外
  if (AMUSEMENT_NO_FOOD_DEEPDIVES.has(deepDive) && primaryType && FOOD_FAMILY_PRIMARY_TYPES.has(primaryType)) return false;
  const allowed = ALLOWED_PRIMARY_TYPES_BY_DEEPDIVE[deepDive];
  if (allowed === undefined) return true;            // 定義なし → 制限しない
  if (!primaryType) return true;                      // 型不明 → 名前フィルタに委ねる
  if (allowed.includes(primaryType)) return true;     // 明示的に許可された型
  if (SPECIFIC_FOOD_PRIMARY_TYPES.has(primaryType)) return false; // 許可外の具体フード型 → 除外
  return true;                                         // restaurant/japanese_restaurant 等の汎用型 → 通す
}
