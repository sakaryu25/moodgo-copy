// 住所文字列から「都道府県＋市区町村」までを抽出する（丁目・番地は落とす）。
// カード等の粗い地名表示用（都道府県だけより一段詳しい）。例:
//   神奈川県横浜市西区みなとみらい2-3-1 → 神奈川県横浜市西区
//   東京都渋谷区神南1-2-3             → 東京都渋谷区
//   北海道札幌市中央区北1条            → 北海道札幌市中央区
//   神奈川県三浦郡葉山町堀内           → 神奈川県三浦郡葉山町
export function toArea(addr: unknown): string {
  const a = String(addr ?? "")
    .replace(/^日本[、,]\s*/, "")
    .replace(/^〒?\s*\d{3}-?\d{4}\s*/, "")
    .trim();
  if (!a) return "";
  // 都道府県? ＋ 市区町村。町/村は「郡○○町」の行政区分だけ拾い、「八王子市子安町」の様な
  // 市内の町名(＝地名)は含めない。市の後は区だけを付ける（例: 横浜市西区）。
  //   A: ○○市(△△区)?  B: ○○郡△△[町村]  C: ○○[区町村]（特別区/独立した町村）
  const m = a.match(/^(東京都|北海道|大阪府|京都府|.{2,3}県)?(?:([^0-9０-９\s]{1,10}?市(?:[^0-9０-９\s]{1,8}?区)?)|([^0-9０-９\s]{1,10}?郡[^0-9０-９\s]{1,8}?[町村])|([^0-9０-９\s]{1,10}?[区町村]))?/);
  const pref = m?.[1] ?? "";
  const muni = m?.[2] ?? m?.[3] ?? m?.[4] ?? "";
  const area = `${pref}${muni}`.trim();
  return area || a.slice(0, 12);   // 抽出できない住所は先頭12文字でフォールバック
}
