# 信頼度の強さ（高いほど優先）
_CONF_RANK = {"high": 3, "medium": 2, "low": 1}


def _match_chains(name):
    out = []
    for key, tags in CHAINS.items():
        if key in name:
            out.extend(tags)
    return out


def _match_cuisine(cuisine_raw):
    out = []
    if not cuisine_raw:
        return out
    for tok in re.split(r"[;,]", cuisine_raw.lower()):
        tok = tok.strip()
        if tok in CUISINE_MAP:
            out.extend(CUISINE_MAP[tok])
    return out


def _match_name(name):
    out = []
    for rx, tags in NAME_RULES:
        if rx.search(name):
            out.extend(tags)
    return out


# 民族系レストラン（「インドレストラン」等）は「レストラン」部分一致で #洋食 が付くが、
# 業態は各国料理。該当タグがあれば #洋食 を外す。
_ETHNIC_RESTAURANT = re.compile(r"(インド|ネパール|中華|中国|韓国|タイ|ベトナム|アジアン?|トルコ|メキシコ|ブラジル)\S{0,4}(レストラン|料理)")
_ETHNIC_GENRE_TAGS = {"#アジア系統", "#中華", "#韓国", "#各国料理", "#インドネパール料理",
                      "#タイ料理", "#ベトナム料理", "#メキシコ料理", "#ブラジル料理"}


def correct_baked_tags(name, tags):
    """既にタグ付け済みのレコードに対し、店名から判定できる明白な誤タグを除去する。
    QA(全国監査)で検出した名前ベースの誤爆を補正。fetch時(derive内)・merge時の両方で適用。
    tags は list/set どちらでも可。list を返す。"""
    name = name or ""
    s = set(tags)
    # ① 洋麺屋(パスタ) が #ラーメン → 除去（麺屋に部分一致していた古いデータ救済）
    if "洋麺屋" in name or "洋麺" in name:
        s.discard("#ラーメン"); s.discard("#こってりラーメン")
    # ② えびそば/油そば/中華そば/まぜそば(=ラーメン) が #うどんそば → 除去
    if "#うどんそば" in s and re.search(r"中華そば|支那そば|油そば|えびそば|まぜそば|焼きそば", name) \
            and not re.search(r"うどん|讃岐", name):
        s.discard("#うどんそば")
    # ③ 民族系レストランが #洋食 → 除去（各国料理タグが付いている場合のみ安全に外す）
    if "#洋食" in s and _ETHNIC_RESTAURANT.search(name) and (s & _ETHNIC_GENRE_TAGS):
        s.discard("#洋食")
    s.add(BASE)  # 念のためベースは保持
    return sorted(s)


def derive_food_tags(osm_tags, name):
    """OSM の tags(dict) と店名 → {tags, tag_confidence, tag_source}。"""
    name = name or ""
    cuisine_raw = osm_tags.get("cuisine", "") if osm_tags else ""
    amenity = (osm_tags.get("amenity", "") if osm_tags else "").lower()

    tags = set([BASE])
    source = "fallback"
    confidence = "low"

    def consider(new_tags, src, conf):
        nonlocal source, confidence
        if not new_tags:
            return
        tags.update(new_tags)
        if _CONF_RANK[conf] > _CONF_RANK[confidence] or (
            _CONF_RANK[conf] == _CONF_RANK[confidence] and source == "fallback"
        ):
            source, confidence = src, conf

    # 1. チェーン辞書（high）
    consider(_match_chains(name), "chain_dictionary", "high")
    # 2. cuisine（high）
    consider(_match_cuisine(cuisine_raw), "cuisine", "high")
    # 3. 店名正規表現（medium）— 深掘りタグの補完にも効く
    consider(_match_name(name), "name_regex", "medium")
    # 4. amenity フォールバック
    if amenity in AMENITY_MAP:
        amen_tags, amen_conf = AMENITY_MAP[amenity]
        consider(amen_tags, "amenity", amen_conf)

    # QA補正（名前ベースの明白な誤タグを除去）
    out_tags = correct_baked_tags(name, tags)
    # 念のため正本チェック（万一の表記揺れを実行時に弾く）
    for t in out_tags:
        if t not in PREDEFINED_TAGS:
            raise ValueError(f"未定義タグを出力しようとしました: {t}")

    return {"tags": out_tags, "tag_confidence": confidence, "tag_source": source}


